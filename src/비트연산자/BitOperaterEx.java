package 비트연산자;

public class BitOperaterEx {
    public static void main(String[] args) {
        byte num1 = 10;
        byte num2 = 10;
        System.out.println(num1 & num2);
        System.out.println(num1 | num2);
        System.out.println(num1 ^ num2);
        System.out.println(~num1);
    }

}
